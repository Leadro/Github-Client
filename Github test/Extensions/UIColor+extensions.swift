import UIKit

extension UIColor {
	static var normalPurple: UIColor {
		return UIColor(named: "purple")!
	}
	
	static var darkPurple: UIColor {
		return UIColor(named: "darkPurple")!
	}
	
	static var lightPurple: UIColor {
		return UIColor(named: "lightPurple")!
	}
}
